public class Operator03 {
public static void main(String[] args) {
int x =20;
int y = 30;
//소괄호가 true if중괄호 실행 , false라면 else문을 실행
if(x==++y||x>y) {System.out.println("happy");}else {System.out.println("unhappy");}
System.out.println(y);

if(x==20|++y==32) {System.out.println("happy");} else {System.out.println("unhappy");}
System.out.println(y);




}
}

