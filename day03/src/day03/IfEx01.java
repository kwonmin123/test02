package day03;

public class IfEx01 {

	public static void main(String[] args) {
		//0~100까지 랜덤정수
		
		int r=(int)(Math.random()*101);
		System.out.println("점수 :"+r);
		if (r>60) {
			System.out.println("60점이상입니다");
			System.out.println("합격입니다");}
		else
		
		{
			System.out.println("60점이하입니다");
			System.out.println("불합격입니다");}

		
		
		
		

		
		
		
	}

}
